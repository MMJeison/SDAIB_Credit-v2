    private void btn_GuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarCambiosActionPerformed
        if(tf_Identificacion.getText().length() < 1 || tf_Nombre.getText().length() < 1){
            JOptionPane.showMessageDialog(null, "Hay campos vacios");
            return;
        }
        Cliente clt = new Cliente(tf_Nombre.getText(), tf_Identificacion.getText());
        for(int i = 0; i < cliente.getCreditos().size(); i++){
            clt.addCredito(cliente.getCreditos().get(i));
        }
        cliente = clt;
        uIVerActInfClienteControlador.iDAOClientes.actualizarRegistro(cliente);
        JOptionPane.showMessageDialog(null, "Se ha actualizado la informacion correctamente");
        btn_EditarIformacion.setEnabled(true);
        btn_GuardarCambios.setEnabled(false);
        btn_Cancelar.setEnabled(false);
        tf_Identificacion.setEditable(false);
        tf_Nombre.setEditable(false);
    }//GEN-LAST:event_btn_GuardarCambiosActionPerformed

