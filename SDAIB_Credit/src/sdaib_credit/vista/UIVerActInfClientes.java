    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(tf_IngresarId.getText().length()<1){
            JOptionPane.showMessageDialog(null, "Debe ingresar la identificacion del cliente");
            return;
        }
        cliente = uIVerActInfClienteControlador.iDAOClientes.getRegistro(tf_IngresarId.getText());
        if(cliente == null){
            JOptionPane.showMessageDialog(null, "La persona con identificaion '" + tf_IngresarId.getText() + "' "
                    + "no es cliente el banco");
            return;
        }
        tf_Identificacion.setText(cliente.getIdentificacion());
        tf_Nombre.setText(cliente.getNombre());
        List<Credito> creditos = new ArrayList<>();
        for(int i = 0; i<cliente.getCreditos().size(); i++){
            creditos.add(uIVerActInfClienteControlador.iDaoCreditos.getRegistro(cliente.getCreditos().get(i)));
        }
        cargarListCreditos(creditos);
        jLabel2.setEnabled(true);
        jLabel3.setEnabled(true);
        jLabel4.setEnabled(true);
        tb_Creditos.setEnabled(true);
        tf_Identificacion.setEnabled(true);
        tf_Nombre.setEnabled(true);
        
        btn_EditarIformacion.setEnabled(true);
    }//GEN-LAST:event_jButton1ActionPerformed

