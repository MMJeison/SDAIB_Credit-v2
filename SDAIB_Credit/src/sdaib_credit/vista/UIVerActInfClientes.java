    private void cargarListCreditos(List<Credito> creditos){
        tb_Creditos.removeAll();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id");
        modelo.addColumn("Tipo");
        modelo.addColumn("Pagado");
        tb_Creditos.setModel(modelo);
        
        for(int i = 0; i < creditos.size(); i++){
            Credito cred = creditos.get(i);
            String[] str = new String[3];
            str[0] = cred.getId();
            str[1] = cred.getTipoCredito();
            str[2] = "NO";
            if(cred.isPagado()) str[2] = "SI";
            modelo.addRow(str);
        }
        
    }
    
    private void evenKeyTyped(java.awt.event.KeyEvent evt) {                                          
        if(!(evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') &&
                evt.getKeyCode() != 8 && evt.getKeyCode() != 127){
            evt.consume();
        }
    } 

